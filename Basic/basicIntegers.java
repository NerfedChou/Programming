package Basic;

public class basicIntegers {
    // Integer is a whole number, it can be positive or negative
    public static void main(String[] args) {
        // Example of an integer
        int a = 5;
        // Print the integer
        System.out.println(a);

        // You can also create an integer directly on System.out.println.
        System.out.println(10); // Prints 10

        // Combining integers
        int b = 3;
        System.out.println(a + b); // Prints 8

        // Lastly you can do anything with integers that you can do with any other object
        int c = 7;
        int d = 2;
        // Addition using the + operator
        System.out.println(c + d); // Prints 9
    }
}
